def _generate_file_section(metadata: dict) -> str:
    """Generate file metadata section."""
    file_info = metadata.get("file", {})
    security = metadata.get("security", {})

    return f"""
    <section id='file-info'>
        <h2>File Information</h2>
        <div class='key-value'>
            <div class='key'>Filename:</div>
            <div class='value'>{file_info.get('filename', 'unknown')}</div>
            <div class='key'>Size:</div>
            <div class='value'>{file_info.get('size_readable', 'unknown')}</div>
            <div class='key'>Created:</div>
            <div class='value'>{file_info.get('created', 'unknown')}</div>
            <div class='key'>Modified:</div>
            <div class='value'>{file_info.get('modified', 'unknown')}</div>
            <div class='key'>PBIX Version:</div>
            <div class='value'>{metadata.get('pbix_version', 'unknown')}</div>
            <div class='key'>SecurityBindings:</div>
            <div class='value'><span class='badge {("success" if security.get("has_security_bindings") else "warning")}'>{security.get("status", "unknown")}</span></div>
        </div>
    </section>
    """


def _generate_visuals_section(metadata: dict) -> str:
    """Generate visuals inventory section."""
    pages = metadata.get("report", {}).get("pages", [])

    html = "<section id='visuals'><h2>Visual Inventory</h2>"

    for page in pages:
        html += f"<h3>{page['name']}</h3>"
        html += "<table><thead><tr><th>ID</th><th>Type</th><th>Title</th><th>Position</th><th>Size</th></tr></thead><tbody>"

        for visual in page.get("visuals", []):
            pos = visual.get("position", {})
            title = visual.get("title", "(no title)")
            title_badge = "success" if visual.get("has_title") else "warning"

            html += f"""
            <tr>
                <td>{visual['id']}</td>
                <td><span class='badge info'>{visual['type']}</span></td>
                <td><span class='badge {title_badge}'>{title}</span></td>
                <td>({pos.get('x', 0)}, {pos.get('y', 0)})</td>
                <td>{pos.get('w', 0)}x{pos.get('h', 0)}</td>
            </tr>
            """

        html += "</tbody></table>"

    html += "</section>"
    return html


def _generate_lineage_section(lineage: dict) -> str:
    """Generate data lineage section."""
    lineages = lineage.get("lineages", [])
    orphaned = lineage.get("orphaned_objects", {})

    html = "<section id='lineage'><h2>Data Lineage</h2>"
    html += f"<p>Lineages traced: {len(lineages)}</p>"

    if lineages:
        html += "<h3>Used Fields</h3><p>Fields with visual bindings detected.</p>"

    if orphaned.get("tables"):
        html += "<h3>Orphaned Objects</h3>"
        for table in orphaned["tables"][:5]:  # Show first 5
            html += f"<div class='violation'><strong>{table['name']}</strong>: {table['reason']}</div>"

    html += "</section>"
    return html


def _generate_violations_section(violations: dict) -> str:
    """Generate violations section."""
    viol_list = violations.get("violations", [])

    html = "<section id='violations'><h2>Consistency Issues</h2>"

    if not viol_list:
        html += "<p>No issues found!</p>"
    else:
        for viol in viol_list[:20]:  # Show first 20
            severity = viol.get("severity", "info")
            html += f"""
            <div class='violation {severity}'>
                <strong>{viol['category'].replace('_', ' ').title()}:</strong> {viol['message']}
                {f"<br><em>Suggestion: {viol['suggestion']}</em>" if viol.get('suggestion') else ""}
            </div>
            """

    html += "</section>"
    return html


def _generate_datamodel_section(metadata: dict) -> str:
    """Generate data model documentation section."""
    tables = metadata.get("datamodel", {}).get("tables", [])

    html = "<section id='datamodel'><h2>Data Model</h2>"
    html += f"<p>Total tables: {len(tables)}</p>"

    for table in tables[:5]:  # Show first 5 tables
        html += f"<h3>{table['name']}</h3>"
        html += f"<p>Columns: {len(table.get('columns', []))}, Measures: {len(table.get('measures', []))}</p>"

        if table.get("measures"):
            html += "<div style='margin-left: 20px; background: #f9f9f9; padding: 10px; border-radius: 4px;'>"
            for measure in table.get("measures", [])[:3]:  # Show first 3 measures
                expr = measure.get("expression", "")[:100]
                html += f"<div><strong>{measure['name']}</strong></div>"
                if expr:
                    html += f"<code>{expr}...</code>"
            html += "</div>"

    html += "</section>"
    return html


# ── Main Function ────────────────────────────────────────────────────────────

def generate_report(metadata_path: str, lineage_path: str = None,
                   violations_path: str = None, output_path: str = None) -> str:
    """
    Generate unified HTML audit report.

    Parameters
    ----------
    metadata_path : str
        Path to metadata.json from pbix_analyzer
    lineage_path : str, optional
        Path to lineage.json from data_lineage
    violations_path : str, optional
        Path to violations.json from consistency_checker
    output_path : str, optional
        Path to save HTML report

    Returns
    -------
    str : HTML content
    """
    with open(metadata_path, "r", encoding="utf-8") as f:
        metadata = json.load(f)

    lineage = {}
    if lineage_path and Path(lineage_path).exists():
        with open(lineage_path, "r", encoding="utf-8") as f:
            lineage = json.load(f)

    violations = {}
    if violations_path and Path(violations_path).exists():
        with open(violations_path, "r", encoding="utf-8") as f:
            violations = json.load(f)

    print(f"\n  Generating HTML report...")

    html = _generate_html_report(metadata, lineage, violations, output_path)

    if output_path:
        print(f"  [OK] Report saved: {output_path}")

    return html


if __name__ == "__main__":
    import sys

    if len(sys.argv) < 2:
        print(__doc__)
        print("Usage: python generate_docs.py <metadata.json> [lineage.json] [violations.json] [output.html]")
        sys.exit(1)

    metadata_path = sys.argv[1]
    lineage_path = sys.argv[2] if len(sys.argv) > 2 else None
    violations_path = sys.argv[3] if len(sys.argv) > 3 else None
    output_path = sys.argv[4] if len(sys.argv) > 4 else None

    try:
        print("=" * 60)
        print("  Documentation Generator")
        print("=" * 60)

        html = generate_report(metadata_path, lineage_path, violations_path, output_path)

        print("\n" + "=" * 60)
        print("  Report complete!")
        print("=" * 60)

    except Exception as e:
        print(f"\n[ERROR] {e}")
        import traceback
        traceback.print_exc()
        sys.exit(1)
