"""
generate_docs.py
----------------
Generate comprehensive HTML audit report from all PBIX analyzers.

Creates a single interactive HTML document with:
  - Executive summary
  - File metadata and health score
  - Visual inventory
  - Data lineage and orphaned objects
  - Consistency violations
  - Data model documentation
  - Recommendations

Orchestrates: pbix_analyzer, data_lineage, consistency_checker, metadata_extractor

Compatible with: PBRS (offline)
"""

import json
from pathlib import Path
from datetime import datetime


def _generate_html_report(metadata: dict, lineage: dict = None,
                          violations: dict = None, output_path: str = None) -> str:
    """Generate comprehensive HTML audit report."""

    lineage = lineage or {}
    violations = violations or {"violations": []}

    # Calculate health score (0-100)
    health = 100
    health -= violations.get("summary", {}).get("errors", 0) * 10
    health -= violations.get("summary", {}).get("warnings", 0) * 3
    health = max(0, min(100, health))

    # Build HTML
    html_parts = [
        "<!DOCTYPE html>",
        "<html>",
        "<head>",
        "<meta charset='UTF-8'>",
        "<meta name='viewport' content='width=device-width, initial-scale=1'>",
        "<title>PBIX Audit Report</title>",
        "<style>",
        _get_css(),
        "</style>",
        "</head>",
        "<body>",
        "<div class='container'>",
    ]

    # Header
    pbix_name = Path(metadata.get("file", {}).get("filename", "unknown.pbix")).stem
    html_parts.append(f"""
    <header class='header'>
        <h1>Power BI Audit Report</h1>
        <p class='subtitle'>{pbix_name}</p>
        <p class='timestamp'>Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}</p>
    </header>
    """)

    # Executive Summary
    html_parts.append(_generate_summary_section(metadata, health, violations, lineage))

    # File Metadata
    html_parts.append(_generate_file_section(metadata))

    # Visuals Inventory
    html_parts.append(_generate_visuals_section(metadata))

    # Data Lineage
    html_parts.append(_generate_lineage_section(lineage))

    # Violations
    html_parts.append(_generate_violations_section(violations))

    # Data Model
    html_parts.append(_generate_datamodel_section(metadata))

    # Footer
    html_parts.append("""
    <footer class='footer'>
        <p>Generated by vizbuilder PBIX Analyzer Suite</p>
    </footer>
    </div>
    </body>
    </html>
    """)

    html_content = "\n".join(html_parts)

    if output_path:
        with open(output_path, "w", encoding="utf-8") as f:
            f.write(html_content)

    return html_content


